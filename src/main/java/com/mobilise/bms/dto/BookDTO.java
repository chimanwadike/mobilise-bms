package com.mobilise.bms.dto;
import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;

import java.util.ArrayList;
import java.util.List;

public class BookDTO {
    private Long id;
    @NotBlank(message = "Title is mandatory")
    @Size(min = 5, message = "Invalid Title: minimum accepted length is 5")
    private String title;
    @NotBlank(message = "ISBN is mandatory")
    @Size(min = 10, message = "Invalid ISBN: Minimum accepted length is 10")
    private String isbn;
    @Min(value = 1000, message = "Invalid PublishYear: must be greater than 999")
    private int publishYear;
    private String publisher;
    private String edition;
    private int pages;
    private List<Long> authorIds = new ArrayList<>();
    private List<Long> genreIds = new ArrayList<>();

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public int getPublishYear() {
        return publishYear;
    }

    public void setPublishYear(int publishYear) {
        this.publishYear = publishYear;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getEdition() {
        return edition;
    }

    public void setEdition(String edition) {
        this.edition = edition;
    }

    public int getPages() {
        return pages;
    }

    public void setPages(int pages) {
        this.pages = pages;
    }

    public List<Long> getAuthorIds() {
        return authorIds;
    }

    public void setAuthorIds(List<Long> authorIds) {
        this.authorIds = authorIds;
    }

    public List<Long> getGenreIds() {
        return genreIds;
    }

    public void setGenreIds(List<Long> genreIds) {
        this.genreIds = genreIds;
    }
}
